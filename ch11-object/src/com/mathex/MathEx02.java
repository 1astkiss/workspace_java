package com.mathex;

public class MathEx02 {

	public static void main(String[] args) {

		System.out.println("오일러의 수: " + Math.E);
		
		System.out.println("원주율: " + Math.PI);
		
		System.out.println("2의 제곱근: " + Math.sqrt(2));
		
		System.out.println("파이의 Degree" + Math.toDegrees(Math.PI));
		System.out.println("파이의 Degree" + Math.pow(2, 2));
		
	}

}
