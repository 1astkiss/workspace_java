package com.mathex;

public class MathEx01 {

	public static void main(String[] args) {
		
		int i = Math.abs(-10);
		System.out.println(i);
		
		double d = Math.ceil(3.3);
		System.out.println(d);
		
		d = Math.floor(5.7);
		System.out.println(d);
		
		i = Math.round(4.7f);
		System.out.println(i);
	}

}
